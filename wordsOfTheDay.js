export const chooseDifferentWordEachDay = () => {
    let today = new Date()
    today.setDate(today.getDate()+3)
    let dia = today.getUTCDate()
    let mes = today.getUTCMonth()
    let ano = today.getUTCFullYear()
    let dayToGetWord = `${ano}/${mes}/${dia}`
    let cont = 0

    for(let i in wordsDict){
        let keys = Object.keys(wordsDict)
        let value = Object.values(wordsDict)

        if(keys[cont] == dayToGetWord){
            let wordOfTheDay = value[cont]
            return wordOfTheDay
        }
        cont++
    }
}

